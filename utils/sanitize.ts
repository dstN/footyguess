/**
 * Sanitize user-facing text to prevent XSS attacks
 * Removes or escapes HTML special characters
 */
export function sanitizeText(text: string | null | undefined): string {
  if (!text) return "";
  return String(text)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#x27;");
}
